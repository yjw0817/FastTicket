-- ============================================================
-- FastTicket DB Schema (MariaDB)
-- 어린이 회관 티켓 예매 시스템
-- ============================================================

-- 기존 테이블 DROP (역순 - FK 의존성 고려)
DROP TABLE IF EXISTS BOOKING_DETAIL;
DROP TABLE IF EXISTS BOOKING;
DROP TABLE IF EXISTS SCHEDULE;
DROP TABLE IF EXISTS TICKET_PRICE;
DROP TABLE IF EXISTS PROGRAM;
DROP TABLE IF EXISTS SEAT;
DROP TABLE IF EXISTS VENUE;
DROP TABLE IF EXISTS MEMBER;

-- ============================================================
-- 1. MEMBER (회원)
-- ============================================================
CREATE TABLE MEMBER (
    MEMBER_ID   VARCHAR(16)   NOT NULL,
    MEMBER_NM   VARCHAR(50)   NOT NULL,
    PASSWORD    VARCHAR(100)  NOT NULL,
    TEL         VARCHAR(20),
    EMAIL       VARCHAR(100),
    USE_YN      CHAR(1)       DEFAULT 'Y',
    REG_DT      DATETIME      DEFAULT NOW(),
    PRIMARY KEY (MEMBER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='회원';

-- ============================================================
-- 2. VENUE (공연장)
-- ============================================================
CREATE TABLE VENUE (
    VENUE_ID    VARCHAR(16)   NOT NULL,
    VENUE_NM    VARCHAR(100)  NOT NULL,
    LOCATION    VARCHAR(200),
    TOTAL_SEATS INT           DEFAULT 0,
    SEAT_TYPE   CHAR(1)       DEFAULT 'F' COMMENT 'F:자유석, A:지정석',
    DESCRIPTION VARCHAR(500),
    USE_YN      CHAR(1)       DEFAULT 'Y',
    REG_USER    VARCHAR(10),
    REG_DT      DATETIME      DEFAULT NOW(),
    PRIMARY KEY (VENUE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='공연장';

-- ============================================================
-- 3. SEAT (좌석 - 지정석 공연장용)
-- ============================================================
CREATE TABLE SEAT (
    SEAT_ID     VARCHAR(16)   NOT NULL,
    VENUE_ID    VARCHAR(16)   NOT NULL,
    SEAT_ROW    VARCHAR(5),
    SEAT_NO     INT,
    SEAT_GRADE  VARCHAR(20)   COMMENT 'VIP, R, S, A',
    USE_YN      CHAR(1)       DEFAULT 'Y',
    PRIMARY KEY (SEAT_ID),
    FOREIGN KEY (VENUE_ID) REFERENCES VENUE(VENUE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='좌석';

-- ============================================================
-- 4. PROGRAM (프로그램)
-- ============================================================
CREATE TABLE PROGRAM (
    PROGRAM_ID   VARCHAR(16)   NOT NULL,
    PROGRAM_NM   VARCHAR(200)  NOT NULL,
    PROGRAM_TYPE VARCHAR(20)   NOT NULL COMMENT 'ADMISSION/PERFORMANCE/MUSICAL',
    VENUE_ID     VARCHAR(16),
    DESCRIPTION  TEXT,
    AGE_LIMIT    VARCHAR(50),
    RUNNING_TIME INT           COMMENT '공연시간(분)',
    POSTER_IMG   VARCHAR(300),
    START_DATE   DATE,
    END_DATE     DATE,
    USE_YN       CHAR(1)       DEFAULT 'Y',
    REG_USER     VARCHAR(10),
    REG_DT       DATETIME      DEFAULT NOW(),
    PRIMARY KEY (PROGRAM_ID),
    FOREIGN KEY (VENUE_ID) REFERENCES VENUE(VENUE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='프로그램';

-- ============================================================
-- 5. TICKET_PRICE (티켓 가격 - 연령별)
-- ============================================================
CREATE TABLE TICKET_PRICE (
    PRICE_ID     VARCHAR(16)   NOT NULL,
    PROGRAM_ID   VARCHAR(16)   NOT NULL,
    PRICE_TYPE   VARCHAR(20)   NOT NULL COMMENT 'ADULT/CHILD/INFANT/SENIOR/DISABLED',
    PRICE_NM     VARCHAR(50)   NOT NULL,
    PRICE        INT           NOT NULL DEFAULT 0,
    USE_YN       CHAR(1)       DEFAULT 'Y',
    PRIMARY KEY (PRICE_ID),
    FOREIGN KEY (PROGRAM_ID) REFERENCES PROGRAM(PROGRAM_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='티켓가격';

-- ============================================================
-- 6. SCHEDULE (회차/일정)
-- ============================================================
CREATE TABLE SCHEDULE (
    SCHEDULE_ID  VARCHAR(16)   NOT NULL,
    PROGRAM_ID   VARCHAR(16)   NOT NULL,
    EVENT_DATE   DATE          NOT NULL,
    EVENT_TIME   VARCHAR(5)    NOT NULL COMMENT 'HH:mm',
    TOTAL_SEATS  INT           NOT NULL,
    AVAIL_SEATS  INT           NOT NULL,
    STATUS       VARCHAR(10)   DEFAULT 'OPEN' COMMENT 'OPEN/CLOSED/CANCELED',
    REG_DT       DATETIME      DEFAULT NOW(),
    PRIMARY KEY (SCHEDULE_ID),
    FOREIGN KEY (PROGRAM_ID) REFERENCES PROGRAM(PROGRAM_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='일정';

-- ============================================================
-- 7. BOOKING (예매)
-- ============================================================
CREATE TABLE BOOKING (
    BOOKING_ID     VARCHAR(16)   NOT NULL,
    SCHEDULE_ID    VARCHAR(16)   NOT NULL,
    MEMBER_ID      VARCHAR(16),
    BOOKER_NM      VARCHAR(50)   NOT NULL,
    BOOKER_TEL     VARCHAR(20)   NOT NULL,
    BOOKER_EMAIL   VARCHAR(100),
    TOTAL_QTY      INT           NOT NULL,
    TOTAL_AMT      INT           NOT NULL DEFAULT 0,
    BOOKING_STATUS VARCHAR(10)   DEFAULT 'RESERVED' COMMENT 'RESERVED/CANCELED/COMPLETED',
    PAYMENT_STATUS VARCHAR(10)   DEFAULT 'PENDING' COMMENT 'PENDING/PAID/REFUNDED',
    REG_DT         DATETIME      DEFAULT NOW(),
    PRIMARY KEY (BOOKING_ID),
    FOREIGN KEY (SCHEDULE_ID) REFERENCES SCHEDULE(SCHEDULE_ID),
    FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MEMBER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='예매';

-- ============================================================
-- 8. BOOKING_DETAIL (예매 상세)
-- ============================================================
CREATE TABLE BOOKING_DETAIL (
    DETAIL_ID   VARCHAR(16)   NOT NULL,
    BOOKING_ID  VARCHAR(16)   NOT NULL,
    PRICE_ID    VARCHAR(16)   NOT NULL,
    SEAT_ID     VARCHAR(16),
    TICKET_AMT  INT           NOT NULL DEFAULT 0,
    CANCEL_YN   CHAR(1)       DEFAULT 'N',
    PRIMARY KEY (DETAIL_ID),
    FOREIGN KEY (BOOKING_ID) REFERENCES BOOKING(BOOKING_ID),
    FOREIGN KEY (PRICE_ID) REFERENCES TICKET_PRICE(PRICE_ID),
    FOREIGN KEY (SEAT_ID) REFERENCES SEAT(SEAT_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='예매상세';

-- ============================================================
-- 인덱스
-- ============================================================
CREATE INDEX IDX_SEAT_VENUE ON SEAT(VENUE_ID);
CREATE INDEX IDX_PROGRAM_VENUE ON PROGRAM(VENUE_ID);
CREATE INDEX IDX_PROGRAM_TYPE ON PROGRAM(PROGRAM_TYPE);
CREATE INDEX IDX_TICKET_PRICE_PROGRAM ON TICKET_PRICE(PROGRAM_ID);
CREATE INDEX IDX_SCHEDULE_PROGRAM ON SCHEDULE(PROGRAM_ID);
CREATE INDEX IDX_SCHEDULE_DATE ON SCHEDULE(EVENT_DATE);
CREATE INDEX IDX_BOOKING_SCHEDULE ON BOOKING(SCHEDULE_ID);
CREATE INDEX IDX_BOOKING_MEMBER ON BOOKING(MEMBER_ID);
CREATE INDEX IDX_BOOKING_DETAIL_BOOKING ON BOOKING_DETAIL(BOOKING_ID);

