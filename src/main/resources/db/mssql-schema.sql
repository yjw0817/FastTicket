);

-- PROGRAM 테이블 (프로그램/공연)
IF NOT EXISTS (SELECT * FROM sys.tables WHERE name = 'PROGRAM')
CREATE TABLE PROGRAM (
    PROGRAM_ID   VARCHAR(16)  NOT NULL PRIMARY KEY,
    PROGRAM_NM   VARCHAR(200),
    PROGRAM_TYPE VARCHAR(50),
    VENUE_ID     VARCHAR(16),
    DESCRIPTION  VARCHAR(500),
    AGE_LIMIT    VARCHAR(20),
    RUNNING_TIME INT,
    POSTER_IMG   VARCHAR(200),
    START_DATE   DATE,
    END_DATE     DATE,
    OPEN_TIME    VARCHAR(5),              -- 운영 시작시간 (HH:mm), NULL이면 기본 영업시간 사용
    CLOSE_TIME   VARCHAR(5),              -- 운영 종료시간 (HH:mm), NULL이면 기본 영업시간 사용
    USE_YN       CHAR(1),
    REG_USER     VARCHAR(10),
    REG_DT       DATETIME DEFAULT GETDATE()
);

-- TICKET_PRICE 테이블 (티켓 가격)
