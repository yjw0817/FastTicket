-- WEEKEND → SATURDAY + SUNDAY 마이그레이션
IF EXISTS (SELECT 1 FROM SESSION_TEMPLATE WHERE DAY_TYPE = 'WEEKEND')
BEGIN
    INSERT INTO SESSION_TEMPLATE (PROGRAM_ID, SESSION_NO, DAY_TYPE, EVENT_TIME, ENABLED, ONLINE_CAP, OFFLINE_CAP)
    SELECT PROGRAM_ID, SESSION_NO, 'SATURDAY', EVENT_TIME, ENABLED, ONLINE_CAP, OFFLINE_CAP
    FROM SESSION_TEMPLATE WHERE DAY_TYPE = 'WEEKEND'
    AND NOT EXISTS (SELECT 1 FROM SESSION_TEMPLATE s2 WHERE s2.PROGRAM_ID = SESSION_TEMPLATE.PROGRAM_ID AND s2.SESSION_NO = SESSION_TEMPLATE.SESSION_NO AND s2.DAY_TYPE = 'SATURDAY');

    INSERT INTO SESSION_TEMPLATE (PROGRAM_ID, SESSION_NO, DAY_TYPE, EVENT_TIME, ENABLED, ONLINE_CAP, OFFLINE_CAP)
    SELECT PROGRAM_ID, SESSION_NO, 'SUNDAY', EVENT_TIME, ENABLED, ONLINE_CAP, OFFLINE_CAP
    FROM SESSION_TEMPLATE WHERE DAY_TYPE = 'WEEKEND'
    AND NOT EXISTS (SELECT 1 FROM SESSION_TEMPLATE s2 WHERE s2.PROGRAM_ID = SESSION_TEMPLATE.PROGRAM_ID AND s2.SESSION_NO = SESSION_TEMPLATE.SESSION_NO AND s2.DAY_TYPE = 'SUNDAY');

    DELETE FROM SESSION_TEMPLATE WHERE DAY_TYPE = 'WEEKEND';
END;

