-- SAMPLE 테이블
IF NOT EXISTS (SELECT * FROM sys.tables WHERE name = 'SAMPLE')
CREATE TABLE SAMPLE (
    ID          VARCHAR(16)  NOT NULL PRIMARY KEY,
    NAME        VARCHAR(50),
    DESCRIPTION VARCHAR(100),
    USE_YN      CHAR(1),
    REG_USER    VARCHAR(10)
);

-- IDS 테이블 (ID 생성용)
IF NOT EXISTS (SELECT * FROM sys.tables WHERE name = 'IDS')
CREATE TABLE IDS (
    TABLE_NAME  VARCHAR(30) NOT NULL PRIMARY KEY,
    NEXT_ID     DECIMAL(30) NOT NULL
);

-- MEMBER 테이블
IF NOT EXISTS (SELECT * FROM sys.tables WHERE name = 'MEMBER')
CREATE TABLE MEMBER (
    MEMBER_ID   VARCHAR(16)  NOT NULL PRIMARY KEY,
    MEMBER_NM   VARCHAR(50),
    PASSWORD    VARCHAR(200),
    TEL         VARCHAR(500),
    TEL_LAST4   VARCHAR(4),
    EMAIL       VARCHAR(100),
    USE_YN      CHAR(1),
    REG_DT      DATETIME DEFAULT GETDATE()
);

-- TEL_LAST4 컬럼 추가 (기존 테이블용)
IF NOT EXISTS (SELECT * FROM sys.columns WHERE object_id = OBJECT_ID('MEMBER') AND name = 'TEL_LAST4')
    ALTER TABLE MEMBER ADD TEL_LAST4 VARCHAR(4);

-- PASSWORD 컬럼 확장 (BCrypt 해시 저장용)
IF EXISTS (SELECT * FROM sys.columns WHERE object_id = OBJECT_ID('MEMBER') AND name = 'PASSWORD' AND max_length < 200)
    ALTER TABLE MEMBER ALTER COLUMN PASSWORD VARCHAR(200);

-- TEL 컬럼 확장 (AES 암호화 저장용)
IF EXISTS (SELECT * FROM sys.columns WHERE object_id = OBJECT_ID('MEMBER') AND name = 'TEL' AND max_length < 500)
    ALTER TABLE MEMBER ALTER COLUMN TEL VARCHAR(500);

-- VENUE 테이블 (공연장)
IF NOT EXISTS (SELECT * FROM sys.tables WHERE name = 'VENUE')
CREATE TABLE VENUE (
    VENUE_ID    VARCHAR(16)  NOT NULL PRIMARY KEY,
    VENUE_NM    VARCHAR(100),
    LOCATION    VARCHAR(200),
    TOTAL_SEATS INT,
    SEAT_TYPE   VARCHAR(50),
    DESCRIPTION VARCHAR(500),
    USE_YN      CHAR(1),
    REG_USER    VARCHAR(10),
    REG_DT      DATETIME DEFAULT GETDATE()
);

-- SEAT 테이블 (좌석)
